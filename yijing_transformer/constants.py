def hex_name(idx: int, short: bool = False) -> str:
    """Return hexagram name by index (0-63)."""
    names = HEX_NAMES_SHORT if short else HEX_NAMES
    if 0 <= idx < 64:
        return names[idx]
    return f"#{idx}"
