# Фонетическая карта meta/solan_lexicon.py (из кода)
META_PHONETIC = {
    # char: (T, B, L, R, D1, D2) bits
    "Г": (0,1,1,0,0,0), "О": (1,0,1,0,1,0), "Р": (1,1,0,0,1,0),
    "А": (1,0,0,0,0,1), "Т": (1,1,0,0,0,0), "У": (0,1,0,1,0,1),
    "М": (1,0,1,0,0,1), "Н": (1,1,0,1,0,0), "Е": (1,0,0,1,0,0),
    "Б": (0,1,1,0,1,0), "О": (1,0,1,0,1,0), "Л": (0,1,0,1,1,0),
    "С": (1,1,0,0,0,1), "К": (0,1,1,0,0,1), "В": (1,0,1,0,0,0),
    "З": (0,1,0,1,0,0), "Д": (1,1,1,0,0,0), "Ж": (0,1,1,1,0,0),
    "П": (1,0,0,1,1,0), "Х": (0,1,0,0,1,1),
}

def meta_encode_word(word: str) -> int:
    """
    Реконструкция кодирования из meta/solan_lexicon.py:
    берём первые два символа → XOR их битов → 6-битный индекс.
    """
    chars = [c for c in word.upper() if c in META_PHONETIC]
    if len(chars) < 2:
        return hash(word) % 64
    b1 = META_PHONETIC[chars[0]]
    b2 = META_PHONETIC[chars[1]]
    bits = tuple(a ^ b for a, b in zip(b1, b2))
    return sum(b << (5 - i) for i, b in enumerate(bits))


def pro2_encode_text(text: str) -> int:
    """pro2: байты → model → dominant hexagram."""
    ids = torch.tensor([b for b in text.encode("utf-8")][:32], dtype=torch.long).unsqueeze(0)
    with torch.no_grad():
        out = model(ids)
        info = out[2] if isinstance(out, tuple) and len(out) > 2 else {}
        hw = info.get("hex_weights")
        if hw is None:
            return 0
        return hw.mean(dim=1).argmax(dim=-1)[0].item()


def hamming_q6(a: int, b: int) -> int:
    """Хэммингово расстояние между двумя Q6-вершинами."""
    bits_a = hexagrams[a].tolist()
    bits_b = hexagrams[b].tolist()
    return sum(1 for x, y in zip(bits_a, bits_b) if abs(x - y) > 0.5)


